package com.rcbsystem.jdk11;

/**
 *
 * @author Rodrigo
 */
public  class Duty {
        private String id;
        private int mainDuty;
        private String additionalDuties;

    public Duty() {
    }

    /**
     * @return the id
     */
    public String getId() {
        return id;
    }

    /**
     * @param id the id to set
     */
    public void setId(String id) {
        this.id = id;
    }

    /**
     * @return the mainDuty
     */
    public int getMainDuty() {
        return mainDuty;
    }

    /**
     * @param mainDuty the mainDuty to set
     */
    public void setMainDuty(int mainDuty) {
        this.mainDuty = mainDuty;
    }

    /**
     * @return the additionalDuties
     */
    public String getAdditionalDuties() {
        return additionalDuties;
    }

    /**
     * @param additionalDuties the additionalDuties to set
     */
    public void setAdditionalDuties(String additionalDuties) {
        this.additionalDuties = additionalDuties;
    }

        
      }
